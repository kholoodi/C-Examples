int a , b ;
a =5;  //assign 5 to a
b = 5; //assign 5 to b
Console.WriteLine(a);
//demonstrate prefix increment operator 
Console.WriteLine(++a); // increment, then display 6
//demonstrate postfix increment operator 
Console.WriteLine(a++); //display 6 then increment 
Console.WriteLine(a); // display 7

//demonstrate prefix decrement  operator 
Console.WriteLine(--b); // decrement, then display 4
//demonstrate postfix decrement operator 
Console.WriteLine(b--); //display 4  then decrement 
Console.WriteLine(b); // display  3